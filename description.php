    <h1>1.Charte de bonne conduite</h1><br><br>
     <p>
     Les dispositions du présent article forment ensemble la « Charte de bonne conduite
      du service d’envoi express entre particuliers et ou professionnels ».    
      Cette Charte est l'esprit même du service de partage de Covalisage entre particuliers et ou professionnels proposé par la Plateforme dont chaque utilisateur est solidaire, responsable et respectueux. Elle s'applique à
       tous les utilisateurs de la Plateforme et du service proposé par la Plateforme.
    </p><br><br>
    <h1>2.Covalisage</h1><br><br>
    <p>
    Transporteur s'engage à ne publier sur la Plateforme que des
     annonces correspondant à des déplacements réellement envisagés.
      Les informations sur la Participation aux Coûts, le lieu de départ, la destination, 
      les types de Bien acceptés, le volume des Bien, leur contenu, etc, et plus généralement tous 
      les détails concernant le Covalisage, à l'auteur de l'annonce sont fournis directement par 
      les utilisateurs et n'engagent pas Colis GP de quelque manière que ce soit. Le Transporteur
       s'engage à
     effectuer le voyage qu'il a publié et à transporter les Biens pour lesquels il s'est engagé.

    </p><br><br>
    <h1>3. Sureté</h1><br><br>
    <p>
    Chaque Expéditeur s'engage à ne pas transmettre de Bien qui pourrait
     compromettre de quelque manière que ce soit la sécurité du Transporteur ou contenir 
     des produits dangereux pouvant altérer sa sureté. Les objets à envoyer doivent être 
     en conformité avec la législation
     en vigueur concernant les objets, produits 
     et substances autorisés dans les aéroports.
    </p><br><br>
    <h1>4. Transparence</h1><br><br>
    <p>
    Un Membre doit être en mesure de présenter immédiatement ses papiers et le contenu du
     Bien à envoyer à un autre Membre dans le cadre d’un Covalisage si celui-ci lui les
      demande. Pour le Transporteur, il s'agit du billet d’avion ou de la confirmation de
       réservation du billet, et son passeport (en cours de validité). Pour un expéditeur, 
       il s'agit de 
    sa pièce d’identité valide (passeport, titre de séjour ou carte d’identité nationale).

    </p><br><br>
    <h1>5. Frais</h1><br><br>
    <p>
    Le ou les expéditeur(s) participe(nt) aux frais 
    selon le montant indiqué sur l'annonce du Transporteur.
    </p><br><br>
    <h1>6. Respect des délais</h1><br><br>
    <p>
    Le Transporteur est tenu et s’engage à aviser le(s) Destinaires(s) à 
    transmettre le(s) Bien(s) dans les 48 Heures suivant son arrivé à destination.
  </p><br><br>
  <h1>7. Loi</h1><br><br>
  <p>
  Les Biens à transporter et les membres sont en règle avec avec la loi. Par ailleurs, 
  les deux parties s'engagent à ne remettre ou transporter
   aucune substance, ni aucun objet illicite ou dangereux
    pendant l’acheminement d’un Bien.
  </p><br><br>
  <h1>8. Propreté et bon état</h1><br><br>
  <p>
  De manière générale, chacune des conditions suivantes doit
   faire l'objet d'un accord entre les covaliseurs, préalablement à
    la réalisation du Covalisage: Participation aux Coûts, date, lieu, destination, volume du Bien, 
    contenu du Bien, lieu de
   rendez-vous de récupération du Bien et de livraison du Bien.
  </p><br><br>
  <h1>10. Informations publiées sur la Plateforme</h1><br><br>
  <p>

  Aucun utilisateur du site ne peut y publier des informations diffamantes, injurieuses ou portant
  atteinte à des tiers. Colis GP supprimera dès en avoir pris connaissance toute information 
  contraire à son éthique. Transporteur et Expéditeur acceptent par avance que les avis 
  les concernant puissent être publiés sur la Plateforme. Ils acceptent par ailleurs que
   leur niveau
   d'expérience soit calculé et publié en fonction des critères décidés par Colis GP.
  </p>




